package mathClassDemo;

public class MathClass {
	public static void main(String[] args) {
		
		//absulate
		int x=Math.abs(-9);
		System.out.println(x);
		
		double y=Math.abs(-9.99);
		System.out.println(y);
		
		//square root,returns double
		System.out.println(Math.sqrt(25));
		
		//power,returns double
		System.out.println(Math.pow(2, 4));
		
		//value of pie
		System.out.println(Math.PI);
		
		//log,10 base,returns double
		System.out.println(Math.log(5));
		
		//exponential
		System.out.println(Math.exp(3));
		
		//max,min
		System.out.println(Math.max(3, 7));
		System.out.println(Math.min(2.34, 2.45));
		
		//floor,ceiling...returns double
		System.out.println(Math.ceil(8.4));
		System.out.println(Math.floor(4.66));
		
		//
		
	}
}
